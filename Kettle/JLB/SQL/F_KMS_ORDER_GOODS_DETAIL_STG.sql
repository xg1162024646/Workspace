DECLARE V_NUM NUMBER;
BEGIN
  SELECT COUNT(1) INTO V_NUM
  FROM ALL_TABLES
  WHERE TABLE_NAME = 'F_KMS_ORDER_GOODS_DETAIL_STG';
  
  IF V_NUM = 1 THEN
    EXECUTE IMMEDIATE 'DROP TABLE F_KMS_ORDER_GOODS_DETAIL_STG';
  END IF;
END;
/
CREATE TABLE F_KMS_ORDER_GOODS_DETAIL_STG
(
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1000000 INCREMENT BY 1) PRIMARY KEY
  , INVOICES_ID NUMBER
  , ORDER_FORM_ID NUMBER
  , ORDER_NUMBER VARCHAR2(100)
  , GOODS_CODE VARCHAR2(100)
  , GOODS_NAME VARCHAR2(2000)
  , UNIT VARCHAR2(10)
  , QUANTITY NUMBER
  , UNIT_PRICE_IN_TAX NUMBER
  , AMOUNT NUMBER
  , BAR_CODE VARCHAR2(100)
  , PURCHASE_PRICE_IS_RIGHT VARCHAR2(100)
  , QUANTITY_IS_RIGHT VARCHAR2(100)
  , REMARK VARCHAR2(2000)
  , FEEDBACK_QUANTITY NUMBER
  , FEEDBACK_PURCHASE_PRICE NUMBER
  , SELECTION VARCHAR2(200)
  , ARRIVE_AMOUNT_IN_TAX NUMBER
  , ARRIVE_AMOUNT_NO_TAX NUMBER
  , ARRIVE_QUANTITY NUMBER
  , ITEM_NUMBER NUMBER
  , PACKING_SPECIFICATION NUMBER
  , ORDER_AMOUNT_NO_TAX NUMBER
  , UNIT_PRICE_NO_TAX NUMBER
  , INSERT_DATE TIMESTAMP DEFAULT SYSTIMESTAMP
  , UPDATE_DATE TIMESTAMP DEFAULT SYSTIMESTAMP
) 
